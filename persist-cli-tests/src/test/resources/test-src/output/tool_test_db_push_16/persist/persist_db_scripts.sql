DROP TABLE IF EXISTS Employee;
DROP TABLE IF EXISTS Company;

CREATE TABLE Company (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	PRIMARY KEY(id)
);

CREATE TABLE Employee (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	companyId INT,
	CONSTRAINT FK_EMPLOYEE_COMPANY_0 FOREIGN KEY(companyId) REFERENCES Company(id),
	PRIMARY KEY(id)
);
