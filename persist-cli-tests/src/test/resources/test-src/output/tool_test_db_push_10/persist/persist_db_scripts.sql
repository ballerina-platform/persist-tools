DROP TABLE IF EXISTS Profiles;
DROP TABLE IF EXISTS Users;

CREATE TABLE Users (
	id INT NOT NULL AUTO_INCREMENT,
	name VARCHAR(191) NOT NULL,
	PRIMARY KEY(id)
);

CREATE TABLE Profiles (
	id INT NOT NULL AUTO_INCREMENT,
	name VARCHAR(191) NOT NULL,
	isAdult BOOLEAN NOT NULL,
	salary FLOAT NOT NULL,
	age DECIMAL NOT NULL,
	PRIMARY KEY(id),
	UNIQUE KEY(name)
)  AUTO_INCREMENT = 5;