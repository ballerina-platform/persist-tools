DROP TABLE IF EXISTS DataTypes;
CREATE TABLE DataTypes (
	a INT NOT NULL AUTO_INCREMENT,
	b1 VARCHAR(191) NOT NULL,
	c1 INT NOT NULL,
	d1 BOOLEAN NOT NULL,
	e1 FLOAT NOT NULL,
	f1 DECIMAL NOT NULL,
	j1 VARCHAR(191) NOT NULL,
	k1 VARCHAR(191) NOT NULL,
	l1 VARCHAR(191) NOT NULL,
	m1 VARCHAR(191) NOT NULL,
	v1 VARCHAR(191) NOT NULL,
	PRIMARY KEY(a)
);