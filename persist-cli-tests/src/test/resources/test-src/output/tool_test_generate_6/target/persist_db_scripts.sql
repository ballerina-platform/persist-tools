DROP TABLE IF EXISTS DataTypes;
CREATE TABLE DataTypes (
	a INT NOT NULL AUTO_INCREMENT,
	b1 VARCHAR(191) NOT NULL,
	c1 INT NOT NULL,
	d1 BOOLEAN NOT NULL,
	e1 FLOAT NOT NULL,
	f1 DECIMAL NOT NULL,
	j1 TIMESTAMP NOT NULL,
	k1 DATETIME NOT NULL,
	l1 DATE NOT NULL,
	m1 TIME NOT NULL,
	PRIMARY KEY(a)
);